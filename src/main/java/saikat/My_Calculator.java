    private void bequalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bequalActionPerformed
        // TODO add your handling code here:
        double ans=0.0;
        
        if(operator=="^2")
        {
            ans=num1*num1;
        }
        else
            num2=Double.parseDouble(t1.getText());
        
        
        switch(operator)
        {
            case "+" :
                ans=num1+num2;
                break;
            case "/" :
                ans=num1/num2;
                break;
            case "*" :
                ans=num1*num2;
                break;
            case "-" :
                ans=num1-num2;
                break;
            case "%" :
                ans=num1%num2;
                break;
            case "âˆš" :
                ans=Math.sqrt(num2);
                break;
        }
        t1.setText(""+ans);
    }//GEN-LAST:event_bequalActionPerformed

